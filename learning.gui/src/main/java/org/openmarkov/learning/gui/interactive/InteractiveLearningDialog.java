    private void btnBlockEditActionPerformed(
            ActionEvent evt) {//GEN-FIRST:event_btnBlockEditActionPerformed
        if (editionsTable.getSelectedEdit() != null) {
            learningManager.getLearningAlgorithm().blockEdit(editionsTable.getSelectedEditProposal());
            updateUI();
        }
    }//GEN-LAST:event_btnBlockEditActionPerformed
    
    private void btnShowBlockedActionPerformed(
            ActionEvent evt) {//GEN-FIRST:event_btnShowBlockedActionPerformed
        blockedEditDialog.setLocation(this.getX() + this.getWidth() - blockedEditDialog.getWidth(),
                                      this.getY() + this.getHeight());
        blockedEditDialog.setVisible(true);
        updateUI();
    }//GEN-LAST:event_btnShowBlockedActionPerformed
    
    private void btnRunActionPerformed(ActionEvent evt) throws IncompatibleEvidenceException.EvidenceIsIncompatibleWithOther, NonProjectablePotentialException, CannotNormalizePotentialException, NotEvaluableNetworkException.NotApplicableNetwork, ConstraintViolatedException {//GEN-FIRST:event_btnRunActionPerformed
        try {
            if (!learningFinished) {
                learningManager.learn();
                this.setVisible(false);
            }
        } finally {
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnRunActionPerformed
    
    private void btnNextPhaseActionPerformed(
            ActionEvent evt) {//GEN-FIRST:event_btnNextPhaseActionPerformed
        learningManager.getLearnedNet().getPNESupport().removeListener(this);
        learningManager.getLearningAlgorithm().runTillNextPhase();
        updateEditionsTable(onlyAllowed, onlyPositive, learningManager.getLearningAlgorithm().getBlockedEdits());
        learningManager.getLearnedNet().getPNESupport().addListener(this);
        updateUI();
    }//GEN-LAST:event_btnNextPhaseActionPerformed
    
    @Override
    public void editionPerformed(EditionEvent editionEvent) throws DoEditException, IncompatibleEvidenceException.EvidenceIsIncompatibleWithOther, NonProjectablePotentialException, NotEvaluableNetworkException.NotApplicableNetwork, CannotNormalizePotentialException, ConstraintViolatedException {
        applySelected();
    }
