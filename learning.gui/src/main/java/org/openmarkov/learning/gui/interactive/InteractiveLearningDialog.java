    public void editionUnblocked(LearningEditProposal edit) {
        learningManager.getLearningAlgorithm().unblockEdit(edit);
        updateUI();
    }
    
    private void applySelected() throws DoEditException, IncompatibleEvidenceException.EvidenceIsIncompatibleWithOther, NonProjectablePotentialException, NotEvaluableNetworkException.NotApplicableNetwork, CannotNormalizePotentialException, ConstraintViolatedException {
        if (editionsTable.getSelectedEdit() != null) {
            dontUpdateUIOnThisEdit = editionsTable.getSelectedEdit();
            learningManager.applyEdit(editionsTable.getSelectedEdit());
            dontUpdateUIOnThisEdit = null;
            updateUI();
        }
    }
    
    private @Nullable PNEdit dontUpdateUIOnThisEdit = null;
    
    public void updateUI() {
        updateEditionsTable(onlyAllowed, onlyPositive, learningManager.getLearningAlgorithm().getBlockedEdits());
        btnRedo.setEnabled(learningManager.getLearnedNet().getPNESupport().getCurrentEditHistory().canRedo());
        btnUndo.setEnabled(learningManager.getLearnedNet().getPNESupport().getCurrentEditHistory().canUndo());
    }
    
    @Override public void afterEditExecutes(@UnknownNullability PNEdit edit) {
        if (dontUpdateUIOnThisEdit != edit) {
            updateUI();
        }
    }
    
    @Override public void afterUndoingEdit(PNEdit edit) {
        if (dontUpdateUIOnThisEdit != edit) {
            updateUI();
        }
    }
    
    private void updateEditionsTable(boolean onlyAllowed, boolean onlyPositive,
                                     List<LearningEditProposal> blockedEdits) {
        ArrayList<LearningEditProposal> showableEditProposals = new ArrayList<>();
        LearningEditProposal bestEdition = learningManager.getLearningAlgorithm().getBestEdit(onlyAllowed, onlyPositive);
        while (showableEditProposals.size() < SHOWING_EDIT_NUM && bestEdition != null) {
            showableEditProposals.add(bestEdition);
            bestEdition = learningManager.getLearningAlgorithm().getNextEdit(onlyAllowed, onlyPositive);
        }
        editionsTable.fill(showableEditProposals);
        if (showableEditProposals.isEmpty()) {
            try {
                learningManager.getLearnedNet().setNetworkType(BayesianNetworkType.getUniqueInstance());
            } catch (ConstraintViolatedException e) {
                throw new UnreachableException(e);
            }
            learningFinished = true;
        }
        if (blockedEditDialog != null) {
            List<LearningEditProposal> blockedEditProposals = new ArrayList<LearningEditProposal>(blockedEdits);
            blockedEditDialog.fill(blockedEditProposals);
        }
        var isFinished = learningFinished && showableEditProposals.isEmpty();
        btnNextPhase.setEnabled(!isFinished);
        btnEdit.setEnabled(!isFinished);
    }
